export function formatPrice(cents: number, locale: Locale): string {
  const value = cents / 100;
  const intl = new Intl.NumberFormat(locale === 'en' ? 'en-IE' : locale === 'de' ? 'de-DE' : 'it-IT', {
    style: 'currency',
    currency: 'EUR',
    minimumFractionDigits: 2,
  });
  return intl.format(value);
}
